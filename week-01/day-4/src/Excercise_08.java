public class Excercise_08 {
    public static void main(String[] args) {
        // Swap the values of the variables
        int a = 123;
        int b = 526;

        System.out.println(a);
        System.out.println(b);

        int t = a; a = b; b = t;

        System.out.println(a);
        System.out.println(b);
    }
}
