@Controller
public class HelloMultiLanguageController {

  @RequestMapping("/sayhello")
  public String greetingWorldWide(Model model) {
    String[] hellos = {"Mirëdita", "Ahalan", "Parev", "Zdravei", "Nei Ho", "Dobrý den", "Ahoj", "Goddag", "Goede dag, Hallo", "Hello", "Saluton", "Hei", "Bonjour",
            "Guten Tag", "Gia'sou", "Aloha", "Shalom", "Namaste", "Namaste", "Jó napot", "Halló", "Helló", "Góðan daginn", "Halo", "Aksunai", "Qanuipit", "Dia dhuit",
            "Salve", "Ciao", "Kon-nichiwa", "An-nyong Ha-se-yo", "Salvëte", "Ni hao", "Dzien' dobry", "Olá", "Bunã ziua", "Zdravstvuyte", "Hola", "Jambo", "Hujambo", "Hej",
            "Sa-wat-dee", "Merhaba", "Selam", "Vitayu", "Xin chào", "Hylo", "Sut Mae", "Sholem " +
            "Aleychem", "Sawubona"};
    List<Strings> helloList = new ArrayList(Arrays.asList(hellos));
    Greeting greeting = new Greeting();
    model.addAttribute("hellos", helloList);
    model.addAttribute("color", greeting.getRandomColor());
    model.addAttribute("fontsize", greeting.getRandomFontSize());
    model.addAttribute("classname", ".first");
    return "hello";
  }

}
